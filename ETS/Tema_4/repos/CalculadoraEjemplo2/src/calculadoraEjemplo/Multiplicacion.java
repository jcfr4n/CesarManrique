package calculadoraEjemplo;

/**
 * Clase para realizar la operación de multiplicación.
 *
 * @author juancfm
 */
public class Multiplicacion {

    /**
     * Calcula el producto de dos números decimales.
     *
     * @param a el primer número decimal a multiplicar
     * @param b el segundo número decimal a multiplicar
     * @return el producto de los dos números decimales
     */
    public static double calcular(double a, double b) {
        return a * b;
    }

}
