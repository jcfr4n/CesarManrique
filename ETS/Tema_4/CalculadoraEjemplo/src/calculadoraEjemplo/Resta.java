package calculadoraEjemplo;

/**
 * Clase para realizar la operación de resta.
 *
 * @author juancfm
 */
public class Resta {

    /**
     * Método que resta dos números decimales.
     *
     * @param a el primer número decimal
     * @param b el segundo número decimal
     * @return la resta de los dos números decimales
     */
    public static double calcular(double a, double b) {
        return a - b;
    }

}
